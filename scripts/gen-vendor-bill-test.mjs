import XLSX from 'xlsx-js-style';
import { format } from 'date-fns';

const selectedMonth = '2026-02';
const customer = {
  customerName: 'John Doe',
  orders: [
    { order_date: '2026-02-01', product: { name: 'Milk' }, quantity: 2, unit: 'ltr', price_per_unit: 30, total_amount: 60, status: 'delivered', vendor: { name: 'Acme' } },
    { order_date: '2026-02-05', product: { name: 'Bread' }, quantity: 1, unit: 'pc', price_per_unit: 20, total_amount: 20, status: 'pending', vendor: { name: 'Acme' } },
    { order_date: '2026-02-10', product: { name: 'Milk' }, quantity: 1, unit: 'ltr', price_per_unit: 30, total_amount: 30, status: 'delivered', vendor: { name: 'Acme' } }
  ]
};

const user = { email: 'vendor@example.com', name: 'Vendor Name' };

try {
  const titleRowCount = 6;
  const [year, month] = selectedMonth.split('-');
  const firstDay = new Date(parseInt(year), parseInt(month) - 1, 1);
  const lastDay = new Date(parseInt(year), parseInt(month), 0);
  const periodString = `${format(firstDay, 'd-MMM-yyyy')} to ${format(lastDay, 'd-MMM-yyyy')}`;
  const generatedBy = user?.email || user?.name || 'Vendor';
  const vendorName = customer.orders[0]?.vendor?.name || 'Vendor';

  const titleRows = [
    ['CUSTOMER BILL'],
    [`Vendor: ${vendorName}`],
    [`Customer: ${customer.customerName || 'N/A'}`],
    [`Order Period: ${periodString}`],
    [`Generated By: ${generatedBy}`],
    [`Generated On: ${format(new Date(), 'dd/MM/yyyy HH:mm:ss')}`],
    []
  ];

  const orderRows = customer.orders.map((order) => [
    format(new Date(order.order_date), 'dd/MM/yyyy'),
    order.product?.name || 'Unknown',
    Number(order.quantity),
    order.unit,
    Number(order.price_per_unit || 0),
    Number(order.total_amount || 0),
    order.status
  ]);

  const headerRow = ['Date', 'Product', 'Quantity', 'Unit', 'Price/Unit', 'Amount', 'Status'];

  const orderDataStartRow = titleRowCount + 2;
  const orderDataEndRow = orderDataStartRow + orderRows.length - 1;
  const productSummaryTitleRow = orderDataEndRow + 2;
  const productSummaryHeaderRow = productSummaryTitleRow + 1;
  const productNames = [...new Set(customer.orders.map((o) => o.product?.name || 'Unknown'))];
  const productSummaryDataStartRow = productSummaryHeaderRow + 1;
  const productSummaryDataEndRow = productSummaryDataStartRow + productNames.length - 1;
  const productGrandTotalRowNum = productSummaryDataEndRow + 1;

  const productUnits = {};
  customer.orders.forEach((o) => {
    const name = o.product?.name || 'Unknown';
    if (!productUnits[name]) productUnits[name] = o.unit || '';
  });

  const productSummaryRows = productNames.map((p) => ([
    '',
    p,
    { f: `COUNTIF(B${orderDataStartRow}:B${orderDataEndRow},"${p}")` },
    productUnits[p] || '',
    { f: `IFERROR(AVERAGEIF(B${orderDataStartRow}:B${orderDataEndRow},"${p}",E${orderDataStartRow}:E${orderDataEndRow}),0)` },
    { f: `SUMIF(B${orderDataStartRow}:B${orderDataEndRow},"${p}",F${orderDataStartRow}:F${orderDataEndRow})` },
    ''
  ]));

  const productGrandTotalRow = [
    '',
    'GRAND TOTAL',
    { f: `SUM(C${productSummaryDataStartRow}:C${productSummaryDataEndRow})` },
    '',
    '',
    { f: `SUM(F${productSummaryDataStartRow}:F${productSummaryDataEndRow})` },
    ''
  ];

  const mainGrandTotalRow = [
    '',
    'GRAND TOTAL',
    { f: `COUNTA(B${orderDataStartRow}:B${orderDataEndRow})` },
    '',
    '',
    { f: `SUM(F${orderDataStartRow}:F${orderDataEndRow})` },
    ''
  ];

  const allData = [
    ...titleRows,
    headerRow,
    ...orderRows,
    [],
    mainGrandTotalRow,
    [],
    ['PRODUCT / STATUS SUMMARY'],
    ['', 'Product', 'Total Orders', 'Unit', 'Price/Unit', 'Total Amount (₹)', 'Status'],
    ...productSummaryRows,
    productGrandTotalRow,
    []
  ];

  const ws = XLSX.utils.aoa_to_sheet(allData);
  ws['!cols'] = Array.from({length:7}, (_,i)=>({wch: Math.max(...allData.map(r=>String(r[i]||'').length))+2}));
  ws['!merges'] = ws['!merges'] || [];
  ws['!merges'].push({ s: { r: productSummaryTitleRow, c: 1 }, e: { r: productSummaryTitleRow, c: 5 } });

  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, 'Bill');
  const outPath = './tmp-vendor-bill.xlsx';
  XLSX.writeFile(wb, outPath);
  console.log('Wrote', outPath);

  const read = XLSX.readFile(outPath);
  console.log('Read sheets:', read.SheetNames);
} catch (err) {
  console.error('Error generating test bill:', err);
}
